#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	i2;

	i2 = ac - 1;
	while (i2 > 0)
	{
		i = 0;
		while (av[i2][i])
		{
			write (1, &av[i2][i], 1);
			i++;
		}
		i2--;
		write (1, "\n", 1);
	}
	return (0);
}
